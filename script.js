const container = document.querySelector(".cards-container");
const form = document.getElementById("tarot-form");
const input = document.getElementById("texto");
const showAllBtn = document.getElementById("show-all");

let tarotCards = []; // Se guarda aquí la data de la API

async function fetchData() {
    try {

        const response = await fetch("https://tarotapi.dev/api/v1/cards");
        const data = await response.json();
        tarotCards = data.cards; // Guardamos para uso posterior
        console.log(data);
       
    } catch (error) {
        console.log(error);
    }

}

form.addEventListener("submit", function (e) {
    e.preventDefault(); // Evita recargar la página
    const question = input.value.trim();
    if (question) {
        showRandomCard();
        input.value = ""; // Limpia el input
    }
});

showAllBtn.addEventListener("click", function () {
    showAllCards();
});

function showRandomCard() {

    if (tarotCards.length === 0) return;// // Si no hay cartas, no hacemos nada

    const randomIndex = Math.floor(Math.random() * tarotCards.length);
    const card = tarotCards[randomIndex];
    const image = tarotCardsImages.find(img => img.name.toLowerCase() === card.name.toLowerCase());


    container.innerHTML = `
        <div class="card">
            <div class="card-header">
                <h3>${card.name}</h3>
                <div class="image-container">
                    <img src="${image.image}" alt="${card.name}">
                </div>
            </div>
            <div class="content">
                    <h4>Meaning:</h4>
                    <p>${card.meaning_up}</p>
                    <h4>Description:</h4>
                    <p>${card.desc}</p>
            </div>
        </div>
    `;
}
function showAllCards() {
    if (tarotCards.length === 0) return; // Si no hay cartas, no hacemos nada
    container.innerHTML = ""; // Limpia el contenedor antes de mostrar todas las cartas
    tarotCards.forEach(card => {

        const image = tarotCardsImages.find(img => img.name.toLowerCase() === card.name.toLowerCase());


        container.innerHTML += `
            <div class="card">
                <div class="card-header">
                    <h3>${card.name}</h3>
                    <div class="image-container">
                        <img src="${image.image}" alt="${card.name}">
                    </div>
                </div>
                <div class="content">
                    <h4>Meaning:</h4>
                    <p>${card.meaning_up}</p>
                    <h4>Description:</h4>
                    <p>${card.desc}</p>
                </div>
            </div>
        `;
    });
}
fetchData();

